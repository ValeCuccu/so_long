#include "libft.h"

int	ft_pnbr(int n)
{
	int		len;
	char	c;

	len = 0;
	if (n == -2147483648)
		return (ft_pstr("-2147483648"), 11);
	if (n < 0)
	{
		len += ft_pchar('-');
		n = -n;
	}
	if (n >= 10)
		len += ft_pnbr(n / 10);
	c = (n % 10) + '0';
	len += ft_pchar(c);
	return (len);
}
